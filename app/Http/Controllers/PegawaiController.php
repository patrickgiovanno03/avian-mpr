    public function getPegawai(Request $request)
    {
        $pegawais = MPegawai::query();

        $pegawais = $pegawais->where(function ($query) use ($request) {
            $query->where('Nama', 'like', '%' . $request->search . '%');
            })
            ->limit(10)
            ->get();

        return response()->json([
            'status' => 'success',
            'pegawais' => $pegawais,
        ]);
    }
